name = "Tony Stark"

#UPPER CASE
print(name.upper())

#lower case
print(name.lower())

print(name)

#Find Ops
print(name.find('y'))
print(name.find('Y'))
print(name.find('Stark'))
print(name.find('stark'))

#Replace
print(name.replace("Tony Stark","Iron Man"))

#To check if a character/string is part of the main string(True/False)
print("Stark" in name)
print("S" in name)
print("s" in name)


#upper() lower() replace() doesnt change the original name
