def add(a,b):
    sum = a + b
    print(f"Sum of {a} and {b} = {sum}")
    
def sub(a,b):
    min = a - b
    print(f"Difference of {a} and {b} = {min}")
    
def multi(a,b):
    pro = a * b
    print(f"Product of {a} and {b} = {pro}")
    
def div(a,b):
    divi = a / b
    print(f"Product of {a} and {b} = {divi}")
        
    